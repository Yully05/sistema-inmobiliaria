    private void btnGuardarRegistroActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            InmueblesPropietario inmuebleModel = new InmueblesPropietario();
            // Validaciones de campos obligatorios
            if (txtCodigo.getText().isEmpty() || txtDescripcion.getText().isEmpty() ||
                    txtPrecio.getText().isEmpty() || txtDireccion.getText().isEmpty() ||
                    txtCiudad.getText().isEmpty() || txtDepartamento.getText().isEmpty() ||
                    txtTamano.getText().isEmpty() || txtCantBanos.getText().isEmpty() ||
                    txtTipoInmueble.getText().isEmpty() || txtEstadoInmueble.getText().isEmpty() ||
                    txtModalidadComercializacion.getText().isEmpty() || txtCedulaPropietario.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Completa los campos obligatorios.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }
            // Crear el objeto modelo con los datos del formulario
            inmuebleModel.setCodigo(Integer.parseInt(txtCodigo.getText()));
            inmuebleModel.setDescripcion(txtDescripcion.getText());
            inmuebleModel.setPrecio(Double.parseDouble(txtPrecio.getText()));
            inmuebleModel.setDireccion(txtDireccion.getText());
            inmuebleModel.setCiudad(txtCiudad.getText());
            inmuebleModel.setDepartamento(txtDepartamento.getText());
            inmuebleModel.setTamano(Double.parseDouble(txtTamano.getText()));
            inmuebleModel.setCant_banos(Integer.parseInt(txtCantBanos.getText()));
            inmuebleModel.setTipoInmueble(Integer.parseInt(txtTipoInmueble.getText()));
            inmuebleModel.setEstadoInmueble(Integer.parseInt(txtEstadoInmueble.getText()));
            inmuebleModel.setModalidadComercializacion(Integer.parseInt(txtModalidadComercializacion.getText()));
            inmuebleModel.setCedulaPropietario(txtCedulaPropietario.getText());
            InmueblesPropietarioController inmuebleController = new InmueblesPropietarioController();
            if (inmuebleEdicion) {
                inmuebleController.actualizar(inmuebleModel);
                JOptionPane.showMessageDialog(this, "Datos actualizados.");
            } else {
                inmuebleController.registrar(inmuebleModel);
                JOptionPane.showMessageDialog(this, "Inmueble registrado.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error en el registro. " + ex.getMessage());
        }
        limpiarCampos();

    }                                                  

