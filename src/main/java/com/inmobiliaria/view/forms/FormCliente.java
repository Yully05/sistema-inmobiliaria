    private void cargarDatosCliente() {
        txtCedula.setText(clienteEditar.getCedula());
        txtCedula.setEnabled(false);
        txtNombres.setText(clienteEditar.getNombres());
        txtApellidos.setText(clienteEditar.getApellidos());
        txtDireccion.setText(clienteEditar.getDireccion());
        txtCorreo.setText(clienteEditar.getCorreo());
        Date fechaNac = Date.from(clienteEditar.getFechaNacimiento().atStartOfDay(ZoneId.systemDefault()).toInstant());
        jDateNacimiento.setDate(fechaNac);
        Date fechaExp = Date.from(clienteEditar.getFechaExpDoc().atStartOfDay(ZoneId.systemDefault()).toInstant());
        jDateExpedicion.setDate(fechaExp);
        if (clienteEditar.getTelefonos().size() > 0) {
            txtTelefono1.setText(clienteEditar.getTelefonos().get(0));
        }
        if (clienteEditar.getTelefonos().size() > 1) {
            txtTelefono2.setText(clienteEditar.getTelefonos().get(1));
        }
    }

